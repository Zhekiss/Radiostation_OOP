package work.ngu.vu;

public class Media {
    private String title;
    private Genre genre;

    public Media() {
        this.title = title;
        this.genre = genre;
    }

    public String getTitle() {
        return title;
    }

    public Genre getGenre() {
        return genre;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public  void setGenre(Genre genre) {
        this.genre = genre;
    }

}
